# Get an integer input from the user
user_input = int(input("Enter an integer: "))

# Divide the input by 2 and store the result as a float
result_float = float(user_input) / 2

# Multiply the float result by 3 and store the output as an integer
final_result = int(result_float * 3)

# Print the final integer result
print("Final result:", final_result)
